package subinfo;

public class subinfo {

	private int subCode;
	private String subName;
	private String subGrade;
	private String subPeople;
	private String subPro;
	private String subTime;
	private String subWhere;
	private int subSum;

	public int getSubCode() {
		return subCode;
	}

	public void setSubCode(int subCode) {
		this.subCode = subCode;
	}

	public String getSubName() {
		return subName;
	}

	public void setSubName(String subName) {
		this.subName = subName;
	}

	public String getSubGrade() {
		return subGrade;
	}

	public void setSubGrade(String subGrade) {
		this.subGrade = subGrade;
	}

	public String getSubPeople() {
		return subPeople;
	}

	public void setSubPeople(String subPeople) {
		this.subPeople = subPeople;
	}

	public String getSubPro() {
		return subPro;
	}

	public void setSubPro(String subPro) {
		this.subPro = subPro;
	}

	public String getSubTime() {
		return subTime;
	}

	public void setSubTime(String subTime) {
		this.subTime = subTime;
	}

	public String getSubWhere() {
		return subWhere;
	}

	public void setSubWhere(String subWhere) {
		this.subWhere = subWhere;
	}

	public int getSubSum() {
		return subSum;
	}

	public void setSubSum(int subSum) {
		this.subSum = subSum;
	}

}
